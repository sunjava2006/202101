package com.wangrui.advance.thread;

public class Account {

	private int id ;
	
	private int amount;
	
	public Account(int id, int amount) {
		this.id = id;
		this.amount = amount;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getId() {
		return id;
	}

	
	
	
	
	
	
}
